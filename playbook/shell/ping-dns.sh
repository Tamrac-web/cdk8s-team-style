#!/bin/bash

ips=("223.5.5.5" "180.76.76.76" "101.101.101.101" "1.1.1.1" "119.29.29.29" "114.114.114.114" "202.136.162.11" "9.9.9.9" "8.8.8.8" "4.2.2.1" "208.67.222.222")

# 遍历数组中的每个 IP 地址
for ip in "${ips[@]}"
do
  # 使用 ping 命令 ping 每个 IP 地址，取 3 次的结果，并通过 awk 获取平均响应时间
  ping -c 3 $ip | tail -1| awk -F'/' '{print "Average response time for " "'$ip'" " is <<<" $5 "ms>>>"}'
done
